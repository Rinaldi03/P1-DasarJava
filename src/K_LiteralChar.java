public class K_LiteralChar {
    public static void main(String[] args) {
        char tab = '\t';
        char neweline = '\n';
        char singleQuete ='\'';
        char doubleQuote ='\"';
        System.out.println("Ini contoh" + tab + "literal tab");
        System.out.println("Ini contoh " + neweline + "literal newline");
        System.out.println("" + singleQuete + "Ini contoh literal single quote" + singleQuete);
        System.out.println(""+ doubleQuote + "ini contoh literal double quote" + doubleQuote);

    }
}
